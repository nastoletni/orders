<?php

declare(strict_types=1);

namespace Nastoletni\Orders\Application\Command;

class DeleteOrderCommand
{
    /**
     * @var string
     */
    private $id;

    public function __construct(string $id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getId(): string
    {
        return $this->id;
    }
}